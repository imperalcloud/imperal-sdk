"""v1.6.0 validator rules — I-SKELETON-LLM-ONLY + ctx.cache contract.

Seven new rules added on top of the V1-V16 contract in ``validator.py``:

* ``SKEL-GUARD-1`` — ``ctx.skeleton.get(...)`` called from a function whose
  decorator is not ``@ext.skeleton`` (alert tools generated by ``alert=True``
  inherit skeleton access).
* ``SKEL-GUARD-2`` — ``ctx.skeleton_data`` referenced anywhere (attribute
  removed in v1.6.0).
* ``SKEL-GUARD-3`` — ``ctx.skeleton.update(...)`` referenced anywhere
  (method removed in v1.6.0; kernel is sole writer).
* ``CACHE-MODEL-1`` — ``ctx.cache.set/get`` with a model class name not
  registered via ``<ext>.cache_model`` (static AST check).
* ``CACHE-TTL-1`` — ``ctx.cache.set(..., ttl_seconds=X)`` where ``X`` is a
  literal int outside ``[5, 300]``.
* ``MANIFEST-SKELETON-1`` — Tool name matches ``skeleton_refresh_*`` or
  ``skeleton_alert_*`` but decorator is ``@ext.tool`` (should be
  ``@ext.skeleton``).
* ``SDK-VERSION-1`` — ``imperal.json`` missing ``sdk_version`` or
  ``sdk_version < "1.6.0"`` when the extension uses v1.6.0 features
  (``ctx.cache``, ``@ext.skeleton``, etc.).

These run off AST + manifest JSON so they fire in CI **before** deploy, without
importing the extension module. The runtime ``validate_extension(ext)`` cannot
catch them — AST is the source of truth for "did the author call
``ctx.skeleton_data``?" regardless of whether that branch ever executes.

Design intent: keep this module independent of the existing V1-V16 visitor so
both remain under the 300-line project cap. The CLI wires both together.
"""
from __future__ import annotations

import ast
import json
import os
from dataclasses import dataclass, field
from typing import Iterable

from imperal_sdk.types.contributions import ALLOWED_PANEL_SLOTS
from imperal_sdk.validator import ValidationIssue

# TTL bounds mirror imperal_sdk/cache/client.py (_TTL_MIN / _TTL_MAX).
_TTL_MIN = 5
_TTL_MAX = 300

# Decorator names recognised as skeleton-context functions. Functions
# registered by @ext.skeleton(...) are the primary case; the kernel-generated
# paired skeleton_alert_* tool inherits the same access by convention.
_SKELETON_DECORATORS = {"skeleton"}

# File globs to skip in source scan — venv + compiled + test fixtures.
_SKIP_DIR_NAMES = {
    "venv",
    ".venv",
    "__pycache__",
    ".git",
    "node_modules",
    "dist",
    "build",
    ".pytest_cache",
    ".mypy_cache",
    ".ruff_cache",
}


@dataclass
class _FunctionContext:
    """Per-function AST bookkeeping used while walking a module."""
    name: str
    decorator_attrs: list[str] = field(default_factory=list)
    is_skeleton_decorated: bool = False


def _decorator_attr_name(dec: ast.expr) -> str | None:
    """Extract the trailing attribute name from a decorator AST node.

    ``@ext.tool("x")``          → ``"tool"``
    ``@ext.skeleton("monitors")`` → ``"skeleton"``
    ``@chat.function(...)``     → ``"function"``
    ``@ext.panel("p")``         → ``"panel"``
    ``@my_helper``              → ``None`` (not attribute form).
    """
    target = dec.func if isinstance(dec, ast.Call) else dec
    if isinstance(target, ast.Attribute):
        return target.attr
    return None


def _iter_py_files(root: str) -> Iterable[str]:
    """Yield every .py file under ``root``, skipping virtualenvs + caches."""
    for dirpath, dirnames, filenames in os.walk(root):
        # in-place prune to skip descent into venv/cache trees
        dirnames[:] = [d for d in dirnames if d not in _SKIP_DIR_NAMES]
        for fname in filenames:
            if fname.endswith(".py"):
                yield os.path.join(dirpath, fname)


def _parse(path: str) -> ast.Module | None:
    try:
        with open(path, encoding="utf-8") as fh:
            src = fh.read()
    except (OSError, UnicodeDecodeError):
        return None
    try:
        return ast.parse(src, filename=path)
    except SyntaxError:
        return None


def _is_ctx_attr(node: ast.AST, attr: str) -> bool:
    """True iff ``node`` is ``ctx.<attr>`` (Attribute of Name 'ctx')."""
    return (
        isinstance(node, ast.Attribute)
        and node.attr == attr
        and isinstance(node.value, ast.Name)
        and node.value.id == "ctx"
    )


def _is_ctx_skeleton_method(node: ast.AST, method: str) -> bool:
    """True iff ``node`` is the attribute ``ctx.skeleton.<method>``.

    Matches the outer Attribute whose value is ``ctx.skeleton``.
    """
    return (
        isinstance(node, ast.Attribute)
        and node.attr == method
        and _is_ctx_attr(node.value, "skeleton")
    )


def _collect_cache_model_names(trees: dict[str, ast.Module]) -> set[str]:
    """Return the set of names passed to ``@<anything>.cache_model("...")``.

    The design doc pins registration to ``<ext>.cache_model`` where ``<ext>``
    is an ``Extension`` instance. Statically we cannot resolve the receiver
    type, so we accept any decorator whose trailing attribute is
    ``cache_model`` and whose first positional arg is a string literal. This
    matches the documented spelling and is strict enough to catch typos.
    """
    names: set[str] = set()
    for tree in trees.values():
        for node in ast.walk(tree):
            if not isinstance(node, (ast.ClassDef, ast.FunctionDef, ast.AsyncFunctionDef)):
                continue
            for dec in node.decorator_list:
                if not isinstance(dec, ast.Call):
                    continue
                attr = _decorator_attr_name(dec)
                if attr != "cache_model":
                    continue
                if dec.args and isinstance(dec.args[0], ast.Constant) and isinstance(dec.args[0].value, str):
                    names.add(dec.args[0].value)
    return names


def _find_cache_call(node: ast.Call) -> str | None:
    """If ``node`` is ``ctx.cache.set(...)`` or ``ctx.cache.get(...)`` return
    the method name, else ``None``.
    """
    func = node.func
    if (
        isinstance(func, ast.Attribute)
        and func.attr in {"set", "get", "get_or_fetch"}
        and _is_ctx_attr(func.value, "cache")
    ):
        return func.attr
    return None


def _extract_cache_model_arg(call: ast.Call, method: str) -> str | None:
    """Pull the model-name argument out of a ``ctx.cache.<method>(...)`` call.

    For ``ctx.cache.set(key, value, ttl_seconds=60)`` the model is inferred
    from ``type(value)``; we can only check this statically when ``value`` is
    a constructor call like ``InboxSummary(...)``. For ``ctx.cache.get(key,
    InboxSummary)`` the model is the second positional arg (a Name).

    Returns the model class name as a string, or ``None`` if we cannot
    determine it from AST (conservative — skip rather than false-positive).
    """
    if method in {"get", "get_or_fetch"}:
        # ctx.cache.get(key, model)  or  ctx.cache.get_or_fetch(key, fetcher, model=...)
        if len(call.args) >= 2 and isinstance(call.args[1], ast.Name):
            return call.args[1].id
        for kw in call.keywords:
            if kw.arg == "model" and isinstance(kw.value, ast.Name):
                return kw.value.id
        return None
    # ctx.cache.set(key, value, ttl_seconds=...) — value is call.args[1]
    if method == "set" and len(call.args) >= 2:
        val = call.args[1]
        if isinstance(val, ast.Call) and isinstance(val.func, ast.Name):
            return val.func.id
        if isinstance(val, ast.Name):
            return val.id
    return None


def _extract_cache_ttl(call: ast.Call) -> int | None:
    """Return literal ``ttl_seconds=`` int arg if present, else ``None``."""
    for kw in call.keywords:
        if kw.arg == "ttl_seconds" and isinstance(kw.value, ast.Constant) and isinstance(kw.value.value, int):
            return kw.value.value
    return None


def _uses_v1_6_0_features(trees: dict[str, ast.Module]) -> bool:
    """True iff any scanned file uses a v1.6.0 feature.

    Signals: any ``ctx.cache.*`` reference, any ``@ext.skeleton`` (or
    ``@*.skeleton``) decorator, any ``ctx.skeleton.get`` call. This drives
    ``SDK-VERSION-1`` so extensions still on the v1.5.x contract don't get
    warned about a missing ``sdk_version`` they don't need.
    """
    for tree in trees.values():
        for node in ast.walk(tree):
            if isinstance(node, ast.Attribute) and _is_ctx_attr(node.value, "cache"):
                return True
            if isinstance(node, ast.Call):
                if _find_cache_call(node):
                    return True
                if isinstance(node.func, ast.Attribute) and _is_ctx_skeleton_method(node.func, "get"):
                    return True
            if isinstance(node, (ast.FunctionDef, ast.AsyncFunctionDef)):
                for dec in node.decorator_list:
                    if _decorator_attr_name(dec) == "skeleton":
                        return True
    return False


def _collect_skeleton_tool_targets(trees: dict[str, ast.Module]) -> list[tuple[ast.FunctionDef | ast.AsyncFunctionDef, ast.Call, str, str]]:
    """Find ``@ext.tool("skeleton_refresh_*" | "skeleton_alert_*")`` targets.

    Returns (func_node, decorator_call, tool_name, file_path) tuples. Each
    one triggers MANIFEST-SKELETON-1.
    """
    hits = []
    for path, tree in trees.items():
        for node in ast.walk(tree):
            if not isinstance(node, (ast.FunctionDef, ast.AsyncFunctionDef)):
                continue
            for dec in node.decorator_list:
                if not isinstance(dec, ast.Call):
                    continue
                if _decorator_attr_name(dec) != "tool":
                    continue
                if not dec.args or not isinstance(dec.args[0], ast.Constant):
                    continue
                tool_name = dec.args[0].value
                if not isinstance(tool_name, str):
                    continue
                if tool_name.startswith("skeleton_refresh_") or tool_name.startswith("skeleton_alert_"):
                    hits.append((node, dec, tool_name, path))
    return hits


