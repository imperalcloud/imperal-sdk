def validate_source_tree(path: str) -> list[ValidationIssue]:
    """Walk ``path`` and apply the AST-based v1.6.0 rules.

    Rules fired from source scan:
    * SKEL-GUARD-1 / 2 / 3
    * CACHE-MODEL-1
    * CACHE-TTL-1
    * MANIFEST-SKELETON-1

    SDK-VERSION-1 lives in :func:`validate_manifest_v1_6_0`.
    """
    issues: list[ValidationIssue] = []
    trees: dict[str, ast.Module] = {}
    for p in _iter_py_files(path):
        t = _parse(p)
        if t is not None:
            trees[p] = t

    registered_models = _collect_cache_model_names(trees)
    # Also collect class names whose definition is decorated by cache_model —
    # the class itself is what gets passed to ctx.cache.set/get, so its Name
    # must round-trip through the registry check.
    registered_class_names: set[str] = set()
    for tree in trees.values():
        for node in ast.walk(tree):
            if isinstance(node, ast.ClassDef):
                for dec in node.decorator_list:
                    if _decorator_attr_name(dec) == "cache_model":
                        registered_class_names.add(node.name)

    for file_path, tree in trees.items():
        issues.extend(_visit_module(file_path, tree, registered_class_names))

    # Manifest rule: tool names that should have been @ext.skeleton.
    for _node, dec, tool_name, file_path in _collect_skeleton_tool_targets(trees):
        section = tool_name.split("_", 2)[-1] if tool_name.startswith("skeleton_refresh_") else tool_name.split("_", 2)[-1]
        issues.append(ValidationIssue(
            rule="MANIFEST-SKELETON-1",
            level="ERROR",
            message=(
                f"Tool {tool_name!r} uses @ext.tool but names a skeleton "
                f"refresh/alert slot. Use @ext.skeleton({section!r}) so the "
                f"kernel auto-wires section metadata."
            ),
            file=_relpath(file_path, path),
            line=getattr(dec, "lineno", 0),
            fix=f"Replace @ext.tool({tool_name!r}) with @ext.skeleton({section!r})",
        ))

    return issues


def _relpath(p: str, root: str) -> str:
    try:
        return os.path.relpath(p, root)
    except ValueError:
        return p


def _visit_module(
    file_path: str,
    tree: ast.Module,
    registered_class_names: set[str],
) -> list[ValidationIssue]:
    """Run SKEL-GUARD-*, CACHE-MODEL-1, CACHE-TTL-1 over one module AST."""
    issues: list[ValidationIssue] = []

    # Walk every function body; track whether it's @*.skeleton-decorated.
    func_stack: list[_FunctionContext] = []

    class V(ast.NodeVisitor):
        def visit_FunctionDef(self, node: ast.FunctionDef) -> None:  # noqa: N802
            self._visit_any_func(node)

        def visit_AsyncFunctionDef(self, node: ast.AsyncFunctionDef) -> None:  # noqa: N802
            self._visit_any_func(node)

        def _visit_any_func(self, node):
            attrs = [a for a in (_decorator_attr_name(d) for d in node.decorator_list) if a]
            ctx = _FunctionContext(
                name=node.name,
                decorator_attrs=attrs,
                is_skeleton_decorated="skeleton" in attrs,
            )
            func_stack.append(ctx)
            try:
                self.generic_visit(node)
            finally:
                func_stack.pop()

        def visit_Attribute(self, node: ast.Attribute) -> None:  # noqa: N802
            # SKEL-GUARD-2: ctx.skeleton_data anywhere.
            if _is_ctx_attr(node, "skeleton_data"):
                issues.append(ValidationIssue(
                    rule="SKEL-GUARD-2",
                    level="ERROR",
                    message="ctx.skeleton_data was removed in v1.6.0.",
                    file=file_path,
                    line=node.lineno,
                    fix="Call `await ctx.skeleton.get(section)` from a @ext.skeleton tool instead.",
                ))
            self.generic_visit(node)

        def _check_panel_slot(self, node: ast.Call) -> None:
            """PANEL-SLOT-1 — flag @ext.panel(slot=<unknown literal>)."""
            func = node.func
            if not (isinstance(func, ast.Attribute) and func.attr == "panel"):
                return
            for kw in node.keywords:
                if kw.arg == "slot" and isinstance(kw.value, ast.Constant):
                    val = kw.value.value
                    if isinstance(val, str) and val not in ALLOWED_PANEL_SLOTS:
                        issues.append(ValidationIssue(
                            rule="PANEL-SLOT-1",
                            level="ERROR",
                            message=(
                                f"@ext.panel(..., slot={val!r}) is not a valid slot. "
                                f"Must be one of {sorted(ALLOWED_PANEL_SLOTS)}. "
                                "Note: 'main' was removed in SDK 3.4.0; use 'center' instead."
                            ),
                            file=file_path,
                            line=node.lineno,
                            fix="Replace slot=... with slot='center'",
                        ))
                    return

        def visit_Call(self, node: ast.Call) -> None:  # noqa: N802
            self._check_panel_slot(node)
            func = node.func

            # SKEL-GUARD-3: ctx.skeleton.update(...)
            if _is_ctx_skeleton_method(func, "update"):
                issues.append(ValidationIssue(
                    rule="SKEL-GUARD-3",
                    level="ERROR",
                    message="ctx.skeleton.update() was removed in v1.6.0; kernel is sole writer.",
                    file=file_path,
                    line=node.lineno,
                    fix="Return the refresh payload from a @ext.skeleton tool; kernel persists it.",
                ))

            # SKEL-GUARD-1: ctx.skeleton.get(...) outside @ext.skeleton scope.
            if _is_ctx_skeleton_method(func, "get"):
                if not (func_stack and func_stack[-1].is_skeleton_decorated):
                    enclosing = func_stack[-1].name if func_stack else "<module>"
                    issues.append(ValidationIssue(
                        rule="SKEL-GUARD-1",
                        level="ERROR",
                        message=(
                            f"ctx.skeleton.get(...) called from {enclosing!r}, "
                            f"which is not decorated with @ext.skeleton. "
                            f"Skeleton access is LLM-only in v1.6.0."
                        ),
                        file=file_path,
                        line=node.lineno,
                        fix="Move the read into a @ext.skeleton tool, or use ctx.cache / ctx.store.",
                    ))

            # CACHE-MODEL-1 + CACHE-TTL-1
            method = _find_cache_call(node)
            if method:
                model_name = _extract_cache_model_arg(node, method)
                if model_name and registered_class_names and model_name not in registered_class_names:
                    issues.append(ValidationIssue(
                        rule="CACHE-MODEL-1",
                        level="ERROR",
                        message=(
                            f"ctx.cache.{method}(...) references model "
                            f"{model_name!r} which is not registered via "
                            f"@<ext>.cache_model(...). Register it or use a "
                            f"known class."
                        ),
                        file=file_path,
                        line=node.lineno,
                        fix=f"Add `@ext.cache_model(\"{model_name.lower()}\") class {model_name}(BaseModel): ...`",
                    ))
                if method == "set":
                    ttl = _extract_cache_ttl(node)
                    if ttl is not None and (ttl < _TTL_MIN or ttl > _TTL_MAX):
                        issues.append(ValidationIssue(
                            rule="CACHE-TTL-1",
                            level="ERROR",
                            message=(
                                f"ctx.cache.set(..., ttl_seconds={ttl}) is "
                                f"outside [{_TTL_MIN}, {_TTL_MAX}]."
                            ),
                            file=file_path,
                            line=node.lineno,
                            fix=f"Clamp ttl_seconds to [{_TTL_MIN}, {_TTL_MAX}].",
                        ))

            self.generic_visit(node)

    V().visit(tree)
    return issues


def validate_manifest_v1_6_0(manifest: dict, source_path: str | None = None) -> list[ValidationIssue]:
    """Apply SDK-VERSION-1 against a parsed ``imperal.json``.

    * Warn if the extension uses v1.6.0 features (detected by scanning the
      source tree at ``source_path``) and the manifest is missing
      ``sdk_version`` or has ``sdk_version < "1.6.0"``.
    * Warn unconditionally if the manifest omits ``sdk_version`` — but tagged
      ``INFO`` instead of ``WARN`` when no v1.6.0 features are detected, so
      the noise floor stays low for pre-1.6 extensions.
    """
    uses_v16 = False
    if source_path and os.path.isdir(source_path):
        trees: dict[str, ast.Module] = {}
        for p in _iter_py_files(source_path):
            t = _parse(p)
            if t is not None:
                trees[p] = t
        uses_v16 = _uses_v1_6_0_features(trees)

    sdk_version = manifest.get("sdk_version")
    issues: list[ValidationIssue] = []
    if sdk_version is None:
        issues.append(ValidationIssue(
            rule="SDK-VERSION-1",
            level="WARN" if uses_v16 else "INFO",
            message=(
                "imperal.json missing 'sdk_version'. "
                + ("Extension uses v1.6.0 features; declare `\"sdk_version\": \"1.6.0\"`." if uses_v16
                   else "Declare 'sdk_version' to pin the SDK contract.")
            ),
            file="imperal.json",
            fix='Add `"sdk_version": "1.6.0"` to imperal.json.',
        ))
    else:
        if _version_lt(str(sdk_version), "1.6.0") and uses_v16:
            issues.append(ValidationIssue(
                rule="SDK-VERSION-1",
                level="WARN",
                message=(
                    f"imperal.json declares sdk_version={sdk_version!r} but "
                    f"the extension uses v1.6.0 features (ctx.cache or "
                    f"@ext.skeleton)."
                ),
                file="imperal.json",
                fix='Bump `"sdk_version"` to `"1.6.0"` in imperal.json.',
            ))
    return issues


def _version_lt(a: str, b: str) -> bool:
    """True iff semver string ``a`` is less than ``b``. Non-strict parse."""
    def parts(s: str) -> tuple[int, ...]:
        head = s.split("-", 1)[0]  # strip pre-release
        return tuple(int(x) for x in head.split(".") if x.isdigit())
    try:
        return parts(a) < parts(b)
    except Exception:
        return False
